<p><strong>Droit à réparation du dommage matériel ou moral</strong><br>Toute personne qui a subi un dommage matériel ou moral du fait de la violation du règlement européen peut obtenir du responsable du traitement (ou du sous-traitant) la réparation de son préjudice.</p>
</div>
</div>

