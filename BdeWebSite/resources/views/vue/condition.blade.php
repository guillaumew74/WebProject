<h1>Conditions d'utilisation de vos données</h1>

<p><strong>Consentement renforcé et transparence</strong></p>

<p><strong>Les données personnelles doivent être :</strong><br>traitées de manière licite, loyale et transparente et collectées pour des finalités déterminées ;<br>
explicites et légitimes ;<br>
adéquates, pertinentes et limitées aux finalités du traitement ;<br>
exactes et tenues à jour ;<br>
conservées de façon temporaire et sécurisée.<br>
Les clients ont un droit d'accès à leurs données et peuvent les rectifier et s'opposer à leur utilisation.<br></p>
</div>
</div>

<div class="page-content-wrapper">
<div class="container-fluid">
<p><strong>Sur demande, l'entreprise qui détient des données personnelles doit informer la personne concernée avec les éléments suivants :</strong><br>identité du responsable du fichier ;<br>
finalité du traitement des données ;<br>
caractère obligatoire ou facultatif des réponses ;<br>
droits d'accès, de rectification, d'interrogation et d'opposition ;<br>
les obligations induites par les transmissions des données.<br></p>
</div>
</div>

<div class="page-content-wrapper">
<div class="container-fluid">
<p><strong>Droit à la portabilité des données</strong><br>Toute personne peut récupérer, sous une forme réutilisable, les données qu'elle a fournies et les transférer ensuite à un tiers (réseau social, par exemple).<br>La portabilité concerne uniquement les données recueillies dans le cadre d'un contrat ou d'un consentement.<br></p>
</div>
</div>

<div class="page-content-wrapper">
<div class="container-fluid">
<p><strong>Droit à l'oubli</strong><br>Toute personne a droit à l'effacement de ses données et au déréférencement (droit de demander à un moteur de recherche de supprimer certains résultats associés à ses noms et prénoms).</p>
</div>
</div>

<div class="page-content-wrapper">
<div class="container-fluid">
<p><strong>Droit à notification</strong><br>En cas de violation de la sécurité des données comportant un risque élevé pour les personnes, le responsable du traitement doit les avertir rapidement, sauf dans certaines situations (données déjà chiffrées, par exemple). Il doit également le notifier à la Commission nationale de l'informatique et des libertés (Cnil) dans les 72 heures.</p>
</div>
</div>

<div class="page-content-wrapper">
